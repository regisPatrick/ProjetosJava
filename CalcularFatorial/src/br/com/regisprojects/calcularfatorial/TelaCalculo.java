    private void spnNumStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_spnNumStateChanged
        // TODO add your handling code here:
        int n = Integer.parseInt(spnNum.getValue().toString());
        int f = 1;
        int c = n;
        String fat = "";
        
        while (c >= 1){
            f *= c;
                if (c >= 2){
                fat += c + " * ";
                }else if (c == 1){
                    fat += c;
                }
            c--;
        }
        if (n == 0){
            fat = "1";
            lblConjunto.setText(fat);
        }
        lblConjunto.setText(fat);
        lblFat.setText(Integer.toString(f));
        lblNumFat.setText(spnNum.getValue().toString());
    }//GEN-LAST:event_spnNumStateChanged

